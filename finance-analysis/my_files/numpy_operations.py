import numpy as np

arr = np.arange(1, 11)
print(arr)
print()

# Elementwise operations
print(arr + arr)
print(arr - arr)
print(arr * arr)
print(arr / arr)
print(1 / arr)
print(arr ** 3)
print(arr + 100)
print(np.sqrt(arr))
print(np.exp(arr))
print(np.max(arr))  # same as arr.max()
print(np.cos(arr))
